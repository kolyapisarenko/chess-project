PIECE_SQUARE_TABLE = {"knight" : knight_scores, "pawn" : pawn_scores, "bishop" : bishop_scores, "rook" : rook_scores, "queen" : queen_scores, "king" : king_scores}

def evaluate(gs):
    white_position = 0
    black_position = 0
    if gs.checkmate:
        if gs.white_to_move:
            return -99999
        else:
            return 99999
    if gs.stealmate:
        return 0
    
    for r in range(config.ROWS):
        for c in range(config.COLS):
            if gs.board[r][c] != "--":
                piece = gs.board[r][c]
                piece_color = piece[0]
                piece_type = piece[2:]  
                if piece_color == "w":
                    white_position += PIECE_SQUARE_TABLE[piece_type][r][c] + PIECE_VALUES[piece_type]
                else:
                    black_position += PIECE_SQUARE_TABLE[piece_type][7 - r][c] + PIECE_VALUES[piece_type]

    return white_position - black_position
